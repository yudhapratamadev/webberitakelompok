                        <p>Rumor Luis Milla yang diincar RANS Cilegon FC belum berakhir, klub milik Raffi Ahmad itu justru kembali membuat heboh karena disebut-sebut bakal mendatangkan pemain bintang Eropa yang pernah membela Real Madrid dan Arsenal. Pemain yang dimaksud itu adalah Mesut Ozil. Semua isu tersebut berawal drai akun Instagram @sepakbolaid, yang mana mereka menyebarkan rumor transfer tersebut. Dalam unggahannya, akun tersebut menyebut Ozil memiliki peluang untuk bertanding di Liga 1 bersama RANS Cilegon FC.<br><br>
                            Mesut Ozil, eks penggawa Tim Nasional (Timnas) Jerman, dikabarkan bakal memperkuat RANS Cilegon FC di kompetisi Liga 1,'' tulis akun @sepakbolaid dalam captionnya, Jumat (7/1/2022).<br><br>
                            'Melalui akun Instagram resmi RANS Cilegon FC, @rans.colegon.official, Raffi Ahmad menyampaikan tentang komunikasi dengan salah satu seorang legenda sepak bola,'' imbuh akun tersebut. Netizen pun langsung membanjiri kolom komentar unggahan tersebut. Hampir semua komentar yang ada, netizen tanpa ragu langsung menyangkal rumor tersebut dan menegaskan bahwa rencana datangnya Ozil hanya untuk launching jersey baru. 
                            'Kalau engga salah diundang cuma untuk launching jersey sama RANS Prestige kalau ga salah,' tulis seorang netizen @iqbalmauulana_.<br><br>
                            <br><br>
                            'Cuma datang sebagai tamu pas launching (jersey), bukan mau jadi pemain RANS,'' tulis akun @mael.lagi. RANS Cilegon FC memang tampak serius untuk mengarungi panasnya kompetisi Liga 1 musim depan. Selain pemain, RANS Cilegon juga disebut bakal memboyong Luis Milla, untuk menduduki kursi pelatih.<br>
                            Seperti yang sudah diketahui, saat ini RANS Cilegon baru saja ditinggal oleh mantan pelatihnya, Rahmad Darmawan, setelah mereka dipastikan lolos ke Liga 1. Coach RD memutuskan untuk bergabung dengan Barito Putera. Tentunya sangat menarik jika Luis Milla atau Mesut Ozil benar-benar akan tampil di Liga 1 musim depan. RANS Cilegon FC sendiri dinilai mungkin melakukan itu semua karena Raffi sendiri sebagai pemilik klub berani jor-joran untuk membuat timnya itu menjadi lebih baik.
                            
